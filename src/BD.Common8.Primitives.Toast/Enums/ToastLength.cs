/// <summary>
///  Defines the duration of a toast notification.
/// </summary>
public enum ToastLength
{
    /// <summary>
    /// Show the view or text notification for a short period of time.
    /// </summary>
    Short = 2000,

    /// <summary>
    /// Show the view or text notification for a long period of time.
    /// </summary>
    Long = 3500,
}